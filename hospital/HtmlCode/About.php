        The Government General Hospital, one of the premier Institutions in the country was started on 16th November
        1664 as a small Hospital to treat the sick soldiers of the East India Company. It was the untiring inspired
        efforts of Sir Edward Winter who was the agent of the company that materialised in the first British Hospital at
        Madras.
        In its early days the Hospital was housed at the Fort St. George and in the next 25 years grew into a formal
        medical facility. Governor Sir. Elihy Yale was instrumental in the development of the Hospital and gave it a new
        premises with in the Fort in 1690.
        The Hospital moved out of the Fort after the Anglo French War and it took 20 long years before it could settle
        in the present permanent place in 1772. By the year 1772 the Hospital was training Europeans, Eurasians and
        natives in allopathic methods of diagnosis and treatment and methods of preparing medicines. These trained
        personnel's were posted to various dispensaries in the district head quarters of the then Madras Presidency to
        assist the qualified doctors. By 1820 the institution had the recognition as the model hospital of the East
        India Company. So in 1827 Dr. D.Mortimar was appointed as the Superintendent of the Hospital.
        A private medical hall run by Dr. Mortimar was regularised as a medical school and it was opened by Sir Fredrick
        Adams, the then Governor of Madras on 2nd February 1835. The Governor promulgated an order to make the school a
        state sponsored one and attached it to the General Hospital.
        In 1842 the Hospital opened its doors to Indians. In the next two decades, the teaching staff had increased, the
        duration of the course extended and the curriculum was made comprehensive. Senior courses spanning 5 years was
        started and private people were also admitted for the first time in the institution. In the early part of 1850
        the school council submitted proposals to the government to accord the status of a College. The Government
        granted this request and from 1st October 1850 it became Madras Medical College.
        The first batch of students graduated in 1852 and were granted the Diploma of Graduate of the Madras
        Medical College. In 1857, it gained affiliation to the University of Madras. After the affiliation in 1857 the
        College moved to be a center of excellence imparting training in all the specialities in the field of medicine
        and surgery and it is now one of the premier institutions in the country with under graduate, post graduate and
        super speciality courses.
        The Courses offered by the Madras Medical College are affiliated from 1988 to the “The Tamilnadu Dr. MGR Medical
        University”
        This College has nine affiliated institutions with hospitals, each one of them recognized for excellence in
        Health care services.
        This College stands with pride for having produced Doyens in the field of Medicine and forget not that you have
        become a part of this great institution now.
